package operator;

public class ShiftOperatorPractise {

	public static void main(String[] args) {
		
		System.out.println(20>>2); // Right shift:-20/2^2, 20/4=5
		System.out.println(20<<2); // Left shift :- 20*2=40, 40*2=80 ...o/p:-80
		
		System.out.println(-20>>2); // Right shift:-20/2^2, 20/4=5
		System.out.println(-20<<2); // Left shift :- 20*2=40, 40*2=80 ...o/p:-80

	}

}
